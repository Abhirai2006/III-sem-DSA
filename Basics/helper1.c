void add()
{
    int a, b, sum;
    printf("Enter the values for a and b (for addition): ");
    scanf("%d %d", &a, &b);
    sum = a + b;
    printf("Sum = %d\n", sum);
}

void subtract()
{
    int a, b, diff;
    printf("Enter the values for a and b (for subtraction): ");
    scanf("%d %d", &a, &b);
    diff = a - b;
    printf("Difference = %d\n", diff);
}
